public class WorkHourEntry {
	// fields
	private String id;
	private String name;
	private String role;
	private int hours;
	
	// constructor
	

	// getters
	public String getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getRole() {
		return role;
	}

	public int getHours() {
		return hours;
	}

	// setters
	public void setId(String id) {
		this.id = id;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setRole(String role) {
		this.role = role;
	}
	
	public void setHours(int hours) {
		this.hours = hours;
	}
	
}
